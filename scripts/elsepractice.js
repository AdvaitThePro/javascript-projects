let grade = prompt("Enter Your Grade:"); // gives a prompt in browser (similar to input)
if (grade >= 90) { // use brackets for conditions
    console.log("You got a A");
} else if (grade >= 80) { // else if = elif
    console.log("You got a B");
} else if (grade >= 70) {
    console.log("You got a C");
} else if (grade >= 60) { 
    console.log("You got a D");
} else {
    console.log("You got a F");
}

